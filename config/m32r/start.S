#define _MACRO_ONLY

#include <m32rasm.inc>
#include <s_services.h>

/**************************/
/* スタートアップルーチン */
/**************************/

	.text
	.align 4
	.globl _start
_start:

	.extern		kernel_start
	.extern		hardware_init_hook

	ldidx	r8

	/* 割込みスタックポインタの初期化 */
	lds		r15, r8, __stack

	/* ハードウェア初期化ルーチンの起動 */
	lds		r0, r8, hardware_init_hook
	beqz	r0, L1
	jl		r0

L1:
	/* .bssセクションのクリア */
	xor		r0, r0
	lds		r1, r8, __sbss
	lds		r2, r8, __ebss
	bl		L2
L2:
	beq		r1, r2, L3
	st		r0, @-r2
	jmp		r14
L3:

	/* .dataセクションの初期化 */
	lds		r1, r8, __sidata
	lds		r2, r8, __eidata
	lds		r3, r8, __sdata

	addi	r3, -4
	beq		r1, r2, L5
L4:	ld		r0, @r1+
	st		r0, @+r3
	bne		r1, r2, L4
L5:

	/* 初期化ルーチンの起動 */
	lds		r0, r8, software_init_hook
	beqz	r0, L6
	jl		r0
L6:

	/* カーネル起動 */
	bl		kernel_start

L7:	bra		L7




begin_table
	entry	__stack
	entry	hardware_init_hook
	entry	software_init_hook
	entry	__sbss
	entry	__ebss
	entry	__sidata
	entry	__eidata
	entry	__sdata
end_table
