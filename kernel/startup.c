/*
 *	カーネルの初期化と終了処理
 */

#include "jsp_kernel.h"
#include "time_event.h"

/*
 *  カーネルの初期化
 */
void
kernel_start()
{
	/*
	 *  ターゲット依存の初期化
	 */
	cpu_initialize();
	sys_initialize();

	/*
	 *  システムログサービスの初期化
	 */
	syslog_initialize();

	/*
	 *  起動メッセージの表示
	 */
	print_banner();

	/*
	 *  各モジュールの初期化
	 *
	 *  タイムイベント管理モジュールは他のモジュールより先に初期化
	 *  する必要がある．
	 */
	tmevt_initialize();
	object_initialize();

	/*
	 *  初期化ルーチンの実行
	 */ 
	call_inirtn();

	/*
	 *  カーネルの動作を開始する．
	 */
	exit_and_dispatch();
}

/*
 *  カーネルの終了処理
 */
void
kernel_exit()
{
	cpu_terminate();
	sys_exit();
}
